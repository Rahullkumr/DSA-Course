# 2D array / list 

arr = [[e for e in range(3)],[2*e for e in range(3)],[2*e-1 for e in range(1,4)]]
# [[0, 1, 2], [0, 2, 4], [1, 3, 5]]

for i in range(3):
    for j in range(3):
        print(arr[i][j], end=" ")
    print()

